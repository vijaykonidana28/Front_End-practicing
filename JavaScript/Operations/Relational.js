// Relational operators in javaScript.
let a = 45, b = 15, c = 13, d = "13";
console.log("A>B : " + (a > b));
console.log("A<B : " + (a < b));
console.log("A<=B : " + (a <= b));
console.log("A>=B : " + (a >= b));
console.log("A==B : " + (a == b));
console.log("A!=B : " + (a != b));
console.log("C==D : " + (c == d));
console.log("C===D: " + (c === d));
