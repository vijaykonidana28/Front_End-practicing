// In JavaScript we can initialize the value with let keyword.
// Later it is possible to change the value to the variable.
let a=13;
console.log(a);
console.log(typeof(a));
a=25;           // Here a changes into multiple datatypes like in the python.
console.log(a);
console.log(typeof(a));
a='vijay';
console.log(a);
console.log(typeof(a));
a=13.45;
console.log(a);
console.log(typeof(a));
a=true;
console.log(a);
console.log(typeof(a));